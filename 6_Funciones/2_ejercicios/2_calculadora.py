'''
Define funciones: sumar(), restar(), multiplicar(), dividir()

Cada función recibe dos números y retorna el resultado

Prueba cada función con ejemplos
'''

def sumar(a,b):
    return a+b

def restar(a,b):
    return a-b

def multiplicar(a,b):
    return a*b

def dividir(a,b):
    return a/b

print(sumar(2,6))

print(restar(5,9))

print(multiplicar(4,10))

print(dividir(45,3))